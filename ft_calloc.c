#include "libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	void	*beans;
	size_t	potato;

	potato = nmemb * size;
	beans = malloc(potato);
	if (beans == NULL)
		return (NULL);
	ft_memset(beans, 0, potato);
	return (beans);
}
